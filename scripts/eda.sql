-- 1- Explore the Database
SELECT 
* 
FROM INFORMATION_SCHEMA.TABLES;

--  Retrieve all columns for a specific table (dim_customers)
SELECT 
*
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'dim_customers';

-- 2- Dimensions Exploration

-- Retrieve a list of unique countries
SELECT DISTINCT 
    country 
FROM gold.dim_customers
ORDER BY country;

-- Retrieve a list of unique categories, subcategories, and products
SELECT DISTINCT 
    category, 
    subcategory, 
    product_name 
FROM gold.dim_products
ORDER BY category, subcategory, product_name;

-- 3- Date Exploration

-- Determine the first and last order date and the total duration in months
SELECT 
    MIN(order_date) AS first_order_date,
    MAX(order_date) AS last_order_date,
    DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS order_range_months
FROM gold.fact_sales;

-- Find the youngest and oldest customer based on birthdate
SELECT
    MIN(birthdate) AS oldest_birthdate,
    DATEDIFF(YEAR, MIN(birthdate), GETDATE()) AS oldest_age,
    MAX(birthdate) AS youngest_birthdate,
    DATEDIFF(YEAR, MAX(birthdate), GETDATE()) AS youngest_age
FROM gold.dim_customers;

-- 4- Measures Exploration

-- Find The Total Sales
SELECT 
SUM(sales_amount) AS total_sales
FROM gold.fact_sales;

-- Find How Many Items are Sold
SELECT 
SUM(quantity) AS total_quantity
FROM gold.fact_sales;

-- Find the Average selling price 
SELECT 
AVG(price) AS average_price
FROM gold.fact_sales;

-- Find the total number of orders
SELECT 
COUNT(DISTINCT order_number) AS total_orders
FROM gold.fact_sales;

-- Find the Total number of products
SELECT 
COUNT(DISTINCT product_key) AS total_products
FROM gold.dim_products;

-- Find the Total number of customers
SELECT 
COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customers;

-- Find the Total number of customers that has placed an order
SELECT 
COUNT(DISTINCT customer_key) AS total_customers_ordered
FROM gold.fact_sales;

-- Generate a report that shows all key metrics
SELECT 'Total Sales' AS measure_name, SUM(sales_amount) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total Quantity' AS measure_name, SUM(quantity) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Average Price' AS measure_name, AVG(price) AS measure_value FROM gold.fact_sales
UNION ALL 
SELECT 'Total Orders' AS measure_name, COUNT(DISTINCT order_number) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total Products' AS measure_name, COUNT(DISTINCT product_key) AS measure_value FROM gold.dim_products
UNION ALL
SELECT 'Total Customers' AS measure_name, COUNT(DISTINCT customer_key) AS measure_value FROM gold.dim_customers
UNION ALL
SELECT 'Total Customers Ordered' AS measure_name, COUNT(DISTINCT customer_key) AS measure_value FROM gold.fact_sales;

